from day13 import compare

import pytest


@pytest.mark.parametrize(
    "first, second, expect",
    [
        ([1, 1, 3, 1, 1], [1, 1, 5, 1, 1], True),
        ([[1], [2, 3, 4]], [[1], 4], True),
        ([9], [[8, 7, 6]], False),
        ([[4, 4], 4, 4], [[4, 4], 4, 4, 4], True),
        ([7, 7, 7, 7], [7, 7, 7], False),
        ([], [3], True),
        ([[[]]], [[]], False),
        ([1, [2, [3, [4, [5, 6, 7]]]], 8, 9], [1, [2, [3, [4, [5, 6, 0]]]], 8, 9], False),
        (
            [
                [4, [[], [2], [4, 1, 5, 1], 5, [2, 7, 5, 7]]],
                [3, [], 3, 8],
                [[4, [4, 7], [4, 7], 5, [5, 3, 1, 5]], 8, [1], 9, 6],
            ],
            [
                [[4]],
                [[[], 0, [9, 5, 2, 9, 5]], 9, [[8, 1, 7, 5], 10], 6, [3, [5, 2], 8, [9, 3, 2, 5]]],
                [[[], 9, 5, [0, 7, 8]]],
            ],
            False,
        ),
    ],
)
def test_compare(first, second, expect):
    assert compare(first, second) == expect
